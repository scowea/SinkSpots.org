<p><br>
  <strong>Dates:</strong> May 15-16 (sat/sun)<br>
  In conjunction with the Upper Clackamas Festival ( <a href="http://www.upperclackamasfestival.org">http://www.upperclackamasfestival.org</a> 
  )<br>
  SinkSpots.org will be holding a downtime gathering at the confluence of the 
  Collawash and the Upper Clackamas starting around noon both days over the weekend...times 
  are very informal. Please contact <a href="mailto:sinkspots@yahoo.com">sinkspots@yahoo.com</a> 
  if interested in meeting up.<Br>
  <br>
  <strong>Spot Info:</strong><br>
  The Collawash- near Estacada, Oregon. <br>
  This is a &quot;up and coming&quot; mystery move spot on the upper clackamas. 
  Not yet fully expored so we cant guarrentee quality downtime...but its an "up and coming spot" so we have high hopes it could be good.<br>
  <br>
  Alternate downtime arena at <a href="http://www.sinkspots.org/index.php?spot_id=264">Twin Fan</a> Campground at the confluence of the Hot 
  Springs Fork of the Collawash with the Collawash, about 4 miles upstream of 
  the Collawash Confluence with the Uppper Clackamas.Gathering at alt spot TBA.<br>
  <br>
  <strong>Directions:</strong><br>
  Two Rivers Picnic Area gives you riverside park and play access to the spot. 
  From here we are 15 minutes upstream from Three Lynx. This spot is at the takeout 
  for the June Creek section of the Upper Clack described here: <a href="http://www.oregonkayaking.net/floodstage/clack_high.html">http://www.oregonkayaking.net/floodstage/clack_high.html</a> 
  <br>
  <br>
  <strong>Camping: </strong>SinkSpots.org has reserved two sites at fish creek... 
  All squirt boaters and friends are welcome.. But there is also great camping 
  at Twin Fan Campground about 4 miles upstream of the Collawash Confluence. <br>
  <br>
  <strong>Informal Comp:</strong> <br>
  Downtime will rule the realm... biggest mystery move will take all glory. We may 
  have a few extra boats laying around if anyone is interested in giving it a 
  go. </p>
<strong>Downriver Run:</strong> We will be squirting down fish to bobs at some 
point over the weekend in glass boats... and throwing a plastic boat in bob's 
hole.. 
