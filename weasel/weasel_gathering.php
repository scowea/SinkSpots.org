    <strong>Dates:</strong> August 15-16 (sat/sun)<br>
	Gathering at spot starting around noon both days.... although some folks may be there earlier.
	<Br>
    <br> 
  <strong>Spot Info:</strong><br>
The Weasel - near Mehama, Oregon.<br>Named for the weasels that sometimes can be seen swimming across the river at the eddy line at this class II section of water.<br>
  <br>
  We have had great luck getting big downtime here at this newly found spot, with many epic, deep, long, easy rides. Its a convergence just like the squirt ranch.  The river splits around a small island and creates a sweet convergence coming off the gravel bar point.  both lefties and righties are very easy here.  the deeper water is on the eddy side, so back cutting to the right can result in deep, very slow rides.  There are several major rocks in the deep zone, but so far it has been a very friendly neighborhood. Very deep and long travel can be had here for those with appetite... and it all plays out into a real chill eddy. <br>
    <br>
    <strong>Directions:</strong><br>
    Take I5 South from portland to salem...take highway 22 east from there;  Just past Mehama, OR, turn right onto North Santiam State Park Road...then take the gravel road down to the river...the spot is at the bottom of the island just downstream. Beware of cops on this road in suuped up chargersars... we have seen a lot of unlucky folks pulled over for speeding on highway 22. The spot is about a hour and a half from Portland.<br>
    <br>
    <strong>Camping:</strong><br>
    The nearest camping is <a href="http://www.blm.gov/or/resources/recreation/files/brochures/Fishermen%20Bend%20Recreation%20Site.pdf">Fishermens Bend Rec Area</a>... a BLM site that is first come first serve.  We will plan on camping here. Continue east on rt22 and you will see it just before you get to Mill City...about 3.7 mi (about 8 mins) on the right.  BUT, if that doesnt work out cause its full, as a plan B, theres always Detroit Lake State Park 30 minutes down the road. Just continue East on highway 22... you'll go past Mill City, Gates, Niagra..and eventually you'll see the dam and lake on your right.  We have site D3 and E25 reserved(sat night) for anyone that wants to campout.   There are probably other free/un designated sites available north of the detroit lake on FS roads, but are at least another 20minutes away.  Check out this <a target=_blank href=http://www.sinkspots.org/weasel/detroit_full.pdf >campground map of detroit lake</a>. There is a overview map showing the North Santiam State Rec (picnic) Area, where the spot is, in relation to the campground. They are about 20 miles apart.<br>
    <br>
