      <h2>Step 1: Sand your seams down to the glass</h2>
      First thing your going to need is alot of beer... and maybe a few friends to stand around and watch and drink your beer while offering witty comments and suggestions. once you have that support system in place, then you can get to the manual labor. why sand off the black epoxy off the seams?
      Two reasons, <br>1. so you can mark directly on the glass with a black pen, and <br> 2. you going to have to redo the outside seams anyway so you have to clean up the surface..
      <br><br>
      i put down masking tape as a guide so i wouldnt eat into the deck or hull too much. i used the old fashioned method and it took an hour, but an electric sander or grinder with a sanding wheel on it would be way faster and easier..
      
