depending on how and what you are trying to trim and what your looking for, you may have different strategies...some take some over all volume out, or more on the stern side or more on the bow side. you have to measure and mark your cuts. dont fuck it up. mystery moves are awesome so be careful.<br><br>
my bow is a bit high, and without a weight in front of my feet and a little bit of water in it, i cant really drop the bow on command. too much west virginia chop. so i started at the back of the cockpit, and went up to almost the center of the footbumps for the length of the cut
<br>it was a 4 foot section total. so 1 foot of taper, to a 2 foot section, then another 1 foot taper.
