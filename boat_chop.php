the tapered section is a 1 foot, very narrow triangle that you will cut out... one on either side. then the middle section is a 2 foot retangle of material that will get cut out. this isnt going to work if you start with a full cut and your trying to get this to sink for a 100lbs person, only more minor trim cuts would this technique work...at some point you may have to just cut it all the way apart but i want to avoid that, because getting the boat back together it a bit more complicated and a pain in the ass.
