i decided i wanted to take 1/2 inch of material out total. so from the center line cut, i measured 1/4 inch up on the deck, and 1/4 inch down on to the hull.. making a mark every couple inches, with the 1 foot taper on either end going from 1/4 inch tapered down to zero at the very end of the center line cut. 
<br><br>Then i took some ribbon masking tape and connected the dots. We then took a red sharpy and coolered in the middle inside of the masking tape, so we could clearly see that any glass with sharpy on it should be cut out. pulling the masking tape back off, there was a clean line where we will cut. 
